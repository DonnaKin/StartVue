<template>
    <select v-model="vmodelValue">
        <option v-for="item in dropdownList" :value="item.value">
            {{ item.displayTxt }}
        </option>
    </select>
</template>

<script lang="ts" setup>
/***********************************************************************************************************/
/*                                             I M P O R T                                                 */
/***********************************************************************************************************/
    import { onMounted, defineProps, toRefs } from 'vue'
	
/***********************************************************************************************************/
/*                                               D A T A                                                   */
/***********************************************************************************************************/
    const vmodelValue = defineModel();

/***********************************************************************************************************/
/*                                            C O M P U T E D                                              */
/***********************************************************************************************************/


/***********************************************************************************************************/
/*                                              P R O P S                                                  */
/***********************************************************************************************************/
    const props = withDefaults(defineProps<{ dropdownList: Dropdown[] }>(), { dropdownList: () => [] as Array<Dropdown> });
    const { dropdownList } = toRefs(props);

/***********************************************************************************************************/
/*                                           L I F E C Y C L E                                             */
/***********************************************************************************************************/
    onMounted(() => {
        vmodelValue.value = dropdownList.value[0].value;
    })

/***********************************************************************************************************/
/*                                             M E T H O D                                                 */
/***********************************************************************************************************/
    

/***********************************************************************************************************/
/*                                               E M I T                                                   */
/***********************************************************************************************************/
    

/***********************************************************************************************************/
/*                                              W A T C H                                                  */
/***********************************************************************************************************/


</script>