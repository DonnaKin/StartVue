<template>
    <input
        :type="type"
        :placeholder="placeholder"
        :value="value"
        :style="{ width: width + 'px !important' }"
        @change="change",
        @keyup.enter="enter"
        @keyup.esc="cancel"
        @focus="focus"
        @keydown="keydown"
    />
</template>

<script lang="ts" setup>
/***********************************************************************************************************/
/*                                             I M P O R T                                                 */
/***********************************************************************************************************/
    import { defineProps, toRefs } from 'vue'
	
/***********************************************************************************************************/
/*                                               D A T A                                                   */
/***********************************************************************************************************/
    const props = withDefaults(defineProps<{type: string, placeholder: string, value: string, width: number }>(), { type: 'text', placeholder: 'Placeholder를 입력하세요', value: '값을 입력하세요', width: 100 });
    const { type, placeholder, value, width } = toRefs(props);

/***********************************************************************************************************/
/*                                            C O M P U T E D                                              */
/***********************************************************************************************************/


/***********************************************************************************************************/
/*                                              P R O P S                                                  */
/***********************************************************************************************************/


/***********************************************************************************************************/
/*                                           L I F E C Y C L E                                             */
/***********************************************************************************************************/


/***********************************************************************************************************/
/*                                             M E T H O D                                                 */
/***********************************************************************************************************/


/***********************************************************************************************************/
/*                                               E M I T                                                   */
/***********************************************************************************************************/
    const emit = defineEmits<{ change: [value: string], enter: [value: string], cancel: [], focus: [], keydown: [] }>();

    const change = () => {
        emit('change', value.value);
    }

    const enter = () => {
        emit('enter', value.value);
    }

    const cancel = () => {
        emit('cancel');
    }

    const focus = () => {
        emit('focus')
    }

    const keydown = () => {
        emit('keydown')
    }

/***********************************************************************************************************/
/*                                              W A T C H                                                  */
/***********************************************************************************************************/
</script>

